#MASM program for 32-bit addition

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
  NUM1 DD 00000004H
  NUM2 DD 00000008H
  SUM DD ?
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX
  
  MOV AX,WORD PTR NUM1
  MOV BX,WORD PTR NUM2
  ADD AX,BX
  MOV WORD PTR SUM,AX
  
  MOV AX,WORD PTR NUM1+2
  MOV BX,WORD PTR NUM2+2
  ADC AX,BX
  MOV WORD PTR SUM+2,AX
  
STOP:
  MOV AH,4CH
  INT 21H
CODE ENDS
END START
