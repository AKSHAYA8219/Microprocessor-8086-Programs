"MASM program to count the no of 1s and 0s"

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
  A DW 1234H
  COUNT1 DW ?
  COUNT0 DW ?
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX
  
  MOV AX,A
  MOV CX,10H
  MOV BX,00H
  MOV DX,00H
  
UP:
  RCL AX,1
  JC ODD
  INC DX
  JMP NEXT
  
ODD:
  INC BX
 
NEXT:
  DEC CX
  JNZ UP
  
MOV COUNT1,BX
MOV COUNT0,DX

STOP:
  MOV AH,4CH
  INT 21H

CODE ENDS
END START
