#MASM program for 32-bit subtraction

ASSUME CS:CODE,DS:DATA

DATA SEGMENT
  NUM1 DD 00000004H
  NUM2 DD 00000004H
  RESULT DD ?
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX
  
  MOV AX,WORD PTR NUM1
  MOV BX,WORD PTR NUM2
  SUB AX,BX
  MOV WORD PTR RESULT,AX
  
  MOV AX,WORD PTR NUM1+2
  MOV BX,WORD PTR NUM2+2
  SBB AX,BX
  MOV WORD PTR RESULT+2,AX

STOP:
  MOV AH,4CH
  INT 21H

CODE ENDS
END START
