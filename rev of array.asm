"MASM program to reverse elements of an array"

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
  A DW 0001H,0002H,0003H,0004H
  RESULT DW 4 DUP(0)
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX
  
  MOV SI,OFFSET A+6
  LEA DI,RESULT
  MOV CX,04H
 
DO:
  MOV AX,[SI]
  MOV [DI],AX
  DEC SI
  DEC SI
  INC DI
  INC DI
  DEC CX
  JNZ DO

STOP:
  MOV AH,4CH
  INT 21H

CODE ENDS
END START
