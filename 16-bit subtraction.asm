#MASM program for 16-bit subtraction

ASSUME CS:CODE, DS:DATA

DATA SEGMENT
  NUM1 DW 2000H
  NUM2 DW 1000H
DATA ENDS

CODE SEGMENT
START:
  MOV AX,DATA
  MOV DS,AX
  
  MOV AX,NUM1
  MOV BX,NUM2
  SUB AX,BX
  
  ADD AX,"00"
  LEA DX,AX
  MOV AH,02H
  INT 21H
STOP:
  MOV AH,4CH
  INT 21H

CODE ENDS
END START
